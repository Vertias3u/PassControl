#!/usr/bin/env node
import fs from "node:fs";
import path from "node:path";

const [root, projectId, offsetText, dashboardPortText] = process.argv.slice(2);
const offset = Number(offsetText);
const dashboardPort = Number(dashboardPortText);
if (!root || !projectId || !Number.isInteger(offset) || offset < 0 || !Number.isInteger(dashboardPort)) {
  throw new Error("Usage: write-local-supabase-config <root> <project-id> <offset> <dashboard-port>");
}

const port = (base) => base + offset;
const config = `# Generated by scripts/dev-stack.sh for local development. Do not commit.
project_id = "${projectId}"

[api]
port = ${port(54321)}

[db]
port = ${port(54322)}
shadow_port = ${port(54320)}

[auth]
site_url = "http://localhost:${dashboardPort}"

[studio]
port = ${port(54323)}

[inbucket]
port = ${port(54324)}
smtp_port = ${port(54325)}
pop3_port = ${port(54326)}

[analytics]
port = ${port(54327)}
vector_port = ${port(54328)}
`;

const target = path.join(root, "supabase", "config.toml");
fs.mkdirSync(path.dirname(target), { recursive: true });
fs.writeFileSync(target, config, { mode: 0o600 });
